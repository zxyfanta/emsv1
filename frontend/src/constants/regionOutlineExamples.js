/**
 * 如何使用这些轮廓数据：
 *
 * 1. 在 VisualizationDashboard.vue 中导入：
 *    import { createRegionOutline } from '@/constants/regionOutline'
 *    import { factoryRegion } from '@/constants/regionOutlineExamples'
 *
 * 2. 在 initScene 函数中创建轮廓：
 *    regionOutline = createRegionOutline(scene, factoryRegion)
 *
 * 3. 自定义轮廓：
 *    - 修改 outerBoundary 数组中的坐标点
 *    - 添加 holes 数组定义内部空洞
 *    - 调整 style 对象改变颜色、高度、透明度等
 *
 * 4. 坐标参考：
 *    - 场景网格：100x100单位（-50到50）
 *    - 每个小格：2单位
 *    - 建议轮廓范围：-40到40之间
 */

/**
 * 从实际地图导入轮廓的步骤：
 *
 * 方法1：使用在线工具
 * 1. 访问 http://geojson.io/
 * 2. 绘制你的区域多边形
 * 3. 导出为 GeoJSON 格式
 * 4. 提取 coordinates 并转换为 [x, z] 格式
 *
 * 方法2：使用 CAD/设计软件
 * 1. 在 AutoCAD/SketchUp 等软件中绘制轮廓
 * 2. 导出关键点坐标
 * 3. 手动转换为 [x, z] 数组格式
 *
 * 方法3：使用地图坐标转换
 * 1. 获取实际经纬度坐标
 * 2. 设置参考点（如：左下角为原点）
 * 3. 按比例缩放到场景坐标系
 */

